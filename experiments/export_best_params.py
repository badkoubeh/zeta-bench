"""Persist the winning hyperparameters from an Optuna HPO sweep into a reusable
Hydra agent config (``configs/agent/{agent}_tuned.yaml``).

After a sweep::

    python experiments/train.py -m --config-name=hpo_sac compute=kaggle_gpu

the Optuna sweeper writes the best trial to ``optimization_results.yaml`` in the
sweep output directory (``hydra.sweep.dir``, default ``multirun/<date>/<time>/``).
This script reads that file, takes the best trial's parameters, and writes a tuned
agent config you can reuse on a *single* run with no further tuning::

    python experiments/export_best_params.py --agent sac
    python experiments/train.py agent=sac_tuned compute=kaggle_gpu

By default it picks the most recent ``multirun/*/*/optimization_results.yaml``;
pass ``--sweep-dir`` (a run dir or the file itself) to choose a specific sweep.

**Algorithm vs hardware split.** Only hardware-independent ``agent.*``
hyperparameters (learning_rate, gamma, tau, ent_coef, ...) are baked into the
tuned config. Hardware-coupled ``compute.*`` params (batch_size, n_steps) are
*reported* but not baked: a value tuned on one machine should not be silently
frozen and reused on another (cpu batch_size=64 vs kaggle_gpu=1024). The exact
override needed to reproduce the tuned trial is printed for you to pass explicitly.

The sweep uses Optuna's default in-memory storage (sqlite/RDB storage is currently
unusable: optuna 2.10 is incompatible with SQLAlchemy 2.x), so the per-sweep
``optimization_results.yaml`` is the persisted source of truth — not a study DB.

This module is glue: it reads config, reads a results file, and writes a file. No
training or business logic lives here.
"""
from __future__ import annotations

import argparse
from copy import deepcopy
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

from omegaconf import DictConfig, OmegaConf

_REPO_ROOT = Path(__file__).resolve().parents[1]
_CONFIGS = _REPO_ROOT / "configs"
_RESULTS_FILE = "optimization_results.yaml"

_AGENT_PREFIX = "agent."
_COMPUTE_PREFIX = "compute."


def route_best_params(
    best_params: dict[str, Any],
) -> tuple[dict[str, Any], dict[str, Any]]:
    """Split flat Optuna param names into baked (``agent.*``) and reported (``compute.*``).

    The sweep param keys are dotted Hydra overrides exactly as declared under
    ``hydra.sweeper.params`` (e.g. ``agent.learning_rate``, ``compute.batch_size``).
    Returns ``(agent_overrides, compute_overrides)`` with the group prefix stripped.
    Keys with any other prefix are deliberately ignored (none exist today).
    """
    agent_overrides: dict[str, Any] = {}
    compute_overrides: dict[str, Any] = {}
    for key, value in best_params.items():
        if key.startswith(_AGENT_PREFIX):
            agent_overrides[key[len(_AGENT_PREFIX) :]] = value
        elif key.startswith(_COMPUTE_PREFIX):
            compute_overrides[key[len(_COMPUTE_PREFIX) :]] = value
    return agent_overrides, compute_overrides


def render_tuned_config(
    base_cfg: DictConfig,
    agent_overrides: dict[str, Any],
    *,
    agent_name: str,
    source: str,
    best_value: float | None,
    compute_overrides: dict[str, Any],
) -> str:
    """Render the ``{agent}_tuned.yaml`` text: base agent config + tuned ``agent.*``.

    Keeps the ``# @package _global_`` directive so the file stays a drop-in member
    of the ``agent`` config group (selectable via ``agent={agent}_tuned``), and
    prepends a provenance header documenting the source sweep, the best objective,
    and the hardware-coupled ``compute.*`` params that were NOT baked.
    """
    merged = deepcopy(base_cfg)
    for sub_key, value in agent_overrides.items():
        OmegaConf.update(merged, f"agent.{sub_key}", value, force_add=True)
    body = OmegaConf.to_yaml(merged)

    ts = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M:%S UTC")
    objective = f"{best_value:.4f}" if best_value is not None else "n/a"
    repro = f"#   python experiments/train.py agent={agent_name}_tuned compute=<profile>"
    if compute_overrides:
        repro += "".join(f" compute.{k}={v}" for k, v in compute_overrides.items())

    header_lines = [
        "# @package _global_",
        f"# AUTO-GENERATED by experiments/export_best_params.py on {ts}.",
        "# Do not edit by hand: re-run the exporter to refresh from a sweep.",
        f"# Source       : {source}",
        f"# Best objective: {objective}",
        "#",
        "# Baked below are the hardware-independent agent.* hyperparameters only.",
        "# Hardware-coupled compute.* params were tuned but NOT baked (they belong to",
        "# the compute profile, e.g. cpu batch_size=64 vs kaggle_gpu=1024):",
    ]
    if compute_overrides:
        pairs = "  ".join(f"compute.{k}={v}" for k, v in compute_overrides.items())
        header_lines.append(f"#   {pairs}")
        header_lines.append("# Pass these explicitly to reproduce the tuned trial exactly:")
    else:
        header_lines.append("#   (none in this sweep)")
        header_lines.append("# Reuse with:")
    header_lines.append(repro)

    # body opens with its own "# @package _global_" line from {agent}.yaml; drop the
    # duplicate so the generated file has exactly one package directive.
    body_lines = [ln for ln in body.splitlines() if ln.strip() != "# @package _global_"]
    return "\n".join(header_lines) + "\n" + "\n".join(body_lines) + "\n"


def resolve_results_file(sweep_dir: Path | None, multirun_root: Path) -> Path:
    """Locate the ``optimization_results.yaml`` to read.

    - ``sweep_dir`` is the results file itself, or a sweep run dir containing it.
    - When omitted, pick the most recent ``<multirun_root>/*/*/optimization_results.yaml``.
    """
    if sweep_dir is not None:
        candidate = sweep_dir if sweep_dir.is_file() else sweep_dir / _RESULTS_FILE
        if not candidate.is_file():
            raise SystemExit(f"no {_RESULTS_FILE} at {candidate}")
        return candidate

    matches = sorted(
        multirun_root.glob(f"*/*/{_RESULTS_FILE}"), key=lambda p: p.stat().st_mtime
    )
    if not matches:
        raise SystemExit(
            f"no {_RESULTS_FILE} found under {multirun_root}/*/*/; run a sweep first "
            "or pass --sweep-dir. (python experiments/train.py -m --config-name=hpo_sac ...)"
        )
    return matches[-1]


def read_best_params(results_file: Path) -> tuple[dict[str, Any], float | None]:
    """Read ``best_params`` (and ``best_value``) from an Optuna results YAML."""
    results = OmegaConf.load(results_file)
    best_params = results.get("best_params")
    if best_params is None:
        raise SystemExit(
            f"{results_file} has no 'best_params' (multi-objective sweeps are not "
            "supported by this exporter)."
        )
    container = OmegaConf.to_container(best_params, resolve=True)
    best_value = results.get("best_value")
    return dict(container), (None if best_value is None else float(best_value))


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Export the best Optuna trial into configs/agent/{agent}_tuned.yaml."
    )
    parser.add_argument("--agent", default="sac", choices=["sac", "ppo"])
    parser.add_argument(
        "--sweep-dir",
        default=None,
        type=Path,
        help="sweep run dir or optimization_results.yaml; default = latest under multirun/",
    )
    parser.add_argument("--multirun-root", default=_REPO_ROOT / "multirun", type=Path)
    parser.add_argument("--configs-dir", default=_CONFIGS, type=Path)
    args = parser.parse_args()

    configs_dir = Path(args.configs_dir)
    results_file = resolve_results_file(args.sweep_dir, Path(args.multirun_root))
    best_params, best_value = read_best_params(results_file)
    agent_overrides, compute_overrides = route_best_params(best_params)

    base_path = configs_dir / "agent" / f"{args.agent}.yaml"
    if not base_path.is_file():
        raise SystemExit(f"no base agent config at {base_path}.")
    base_cfg = OmegaConf.load(base_path)
    text = render_tuned_config(
        base_cfg,
        agent_overrides,
        agent_name=args.agent,
        source=str(results_file),
        best_value=best_value,
        compute_overrides=compute_overrides,
    )

    out_path = configs_dir / "agent" / f"{args.agent}_tuned.yaml"
    out_path.write_text(text)

    objective = "n/a" if best_value is None else f"{best_value:.4f}"
    print(f"source: {results_file}  (objective = {objective})")
    print("baked agent.* params:")
    for key, value in agent_overrides.items():
        print(f"  agent.{key} = {value}")
    if compute_overrides:
        print("hardware-coupled compute.* params (NOT baked; pass explicitly):")
        for key, value in compute_overrides.items():
            print(f"  compute.{key} = {value}")
    print(f"wrote {out_path}")
    repro = f"python experiments/train.py agent={args.agent}_tuned compute=<profile>"
    repro += "".join(f" compute.{k}={v}" for k, v in compute_overrides.items())
    print(f"reuse:  {repro}")


if __name__ == "__main__":
    main()
